export function getLocationById(id) {
  const allLocations = getAllLocations();
  return allLocations.find((loc) => loc.id === id);
}

export function getLocationsByType(type) {
  const allLocations = getAllLocations();
  return allLocations.filter((loc) => loc.type === type);
}

export function getLocationsByTags(tags) {
  const allLocations = getAllLocations();
  return allLocations.filter(
    (loc) =>
      Array.isArray(loc.tags) && tags.some((tag) => loc.tags.includes(tag))
  );
}

export function getIndoorLocations() {
  const allLocations = getAllLocations();
  return allLocations.filter((loc) => loc.indoor === true);
}

